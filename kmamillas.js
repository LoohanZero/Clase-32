// Kilómetros a millas

// Crear un programa que pida ingresar una cantidad de kilómetros y muestre un mensaje con el resultado de la conversión en millas.

const kilometros = prompt("Ingrese la cantidad de kilómetros que desea convertir a millas");

const millas = kilometros / "1.609";

alert(`Los ${kilometros} kilómetros ingresados equivalen a ${millas} millas.`)
